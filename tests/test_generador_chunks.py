@freeze_time("2025-06-01 12:00:00")
def test_coverage_buffer_respects_query_age(company_session: Session):
    """El coverage_buffer debe excluir queries muy recientes."""
    start = datetime(2025, 1, 1)
    end = datetime(2025, 1, 31)
    download_type = DownloadType.ISSUED
    chunk_size = timedelta(days=5)
    coverage_buffer = timedelta(days=3)

    # Query procesada muy recientemente (created_at muy cerca del rango)
    # Con freezegun en 2025-06-01, created_at será 2025-06-01
    query = SATQuery(
        download_type=download_type,
        request_type=RequestType.CFDI,
        state=QueryState.PROCESSED,
        start=datetime(2025, 1, 10),
        end=datetime(2025, 1, 20),
    )
    company_session.add(query)
    company_session.flush()

    chunks = get_chunks_no_ws_query_processed(
        company_session,
        start,
        end,
        download_type,
        coverage_buffer=coverage_buffer,
        chunk_size=chunk_size,
    )

    # El buffer debe reducir el rango cubierto por la query
    # La query cubre menos porque created_at - buffer reduce covered_end
    assert len(chunks) > 0


