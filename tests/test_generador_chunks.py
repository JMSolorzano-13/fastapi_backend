def dates_close(d1: datetime, d2: datetime, delta: timedelta = timedelta(seconds=1)):
    return abs(d1 - d2) <= delta


@freeze_time("2026-01-02")
def test_complete_cfdis_generate_chunks(
    company_session: Session,
    company: Company,
):
    start = datetime.fromisoformat("2025-01-01")
    end = mx_now()
    download_type = DownloadType.ISSUED

    assert company_session.query(CFDI).count() == 0, "This test asume no CFDI created"
    assert (
        company_session.query(SATQuery)
        .filter(
            SATQuery.download_type == download_type,
            SATQuery.request_type == RequestType.CFDI,
            SATQuery.state == QueryState.PROCESSED,
        )
        .count()
        == 0
    ), "This test asume no request are processed, ergo, needs to create requests"

    # Test
    chunks = get_cfdi_chunks(
        company_session=company_session,
        company_identifier=company.identifier,
        download_type=download_type,
        start=start,
        end=end,
    )

    # Check results
    first_query = chunks[0]
    last_query = chunks[-1]

    assert dates_close(first_query.start, start)
    assert dates_close(last_query.end, end)


