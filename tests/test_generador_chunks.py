@freeze_time("2025-06-01 12:00:00")
def test_non_processed_queries_ignored(company_session: Session):
    """Queries que no están en estado PROCESSED deben ser ignoradas."""
    start = datetime(2025, 1, 1)
    end = datetime(2025, 1, 31)
    download_type = DownloadType.ISSUED
    chunk_size = timedelta(days=10)

    # Crear queries en diferentes estados (no PROCESSED)
    for state in [QueryState.DRAFT, QueryState.SENT, QueryState.DOWNLOADED]:
        query = SATQuery(
            download_type=download_type,
            request_type=RequestType.CFDI,
            state=state,
            start=datetime(2025, 1, 5),
            end=datetime(2025, 1, 15),
        )
        company_session.add(query)
    company_session.flush()

    chunks = get_chunks_no_ws_query_processed(
        company_session, start, end, download_type, chunk_size=chunk_size
    )

    # Debe retornar el rango completo porque las queries no están procesadas
    assert len(chunks) == (end - start) / chunk_size, (
        "Should cover full range as no queries are PROCESSED"
    )
    assert chunks[0].start == start
    assert chunks[-1].end == end
    assert all(c.end - c.start <= chunk_size for c in chunks)


@freeze_time("2025-06-01 12:00:00")
def test_contiguous_processed_queries_create_gaps(company_session: Session):
    """Queries procesadas contiguas deben crear un solo gap cubierto."""
    start = datetime(2025, 1, 1)
    end = datetime(2025, 1, 31)
    download_type = DownloadType.ISSUED
    chunk_size = timedelta(days=1)

    # Crear queries procesadas contiguas que cubren del 10 al 20
    query1 = SATQuery(
        download_type=download_type,
        request_type=RequestType.CFDI,
        state=QueryState.PROCESSED,
        start=datetime(2025, 1, 10),
        end=datetime(2025, 1, 30),
    )

    query2 = SATQuery(
        download_type=download_type,
        request_type=RequestType.CFDI,
        state=QueryState.PROCESSED,
        start=datetime(2025, 1, 15),
        end=datetime(2025, 1, 20),
    )

    company_session.add_all([query1, query2])
    company_session.flush()

    chunks = get_chunks_no_ws_query_processed(
        company_session, start, end, download_type, chunk_size=chunk_size
    )

    # Debe haber gaps antes y después del rango cubierto
    assert len(chunks) > 0
    # Verificar que no hay chunks en el rango cubierto (10-20)
    chunks_in_covered_range = [
        c for c in chunks if c.start >= datetime(2025, 1, 10) and c.end <= datetime(2025, 1, 20)
    ]
    assert len(chunks_in_covered_range) == 0, "Should not have chunks in the covered range"


